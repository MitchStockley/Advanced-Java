package lesson4;

public class TestMathStaticMethods {
    public static void main(String[] args) {
        System.out.println(Math.random());
        System.out.println("square root: " + Math.sqrt(9));
        System.out.println("round: " + Math.round(Math.random() * 100));
        System.out.println("Absolute: " + Math.abs(9));
    }
}
